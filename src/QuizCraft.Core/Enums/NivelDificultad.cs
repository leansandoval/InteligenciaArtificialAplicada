namespace QuizCraft.Core.Enums;

/// <summary>
/// Enumeración que define los niveles de dificultad para las flashcards
/// </summary>
public enum NivelDificultad
{
    /// <summary>
    /// Nivel muy fácil - preguntas básicas o introductorias
    /// </summary>
    MuyFacil = 1,

    /// <summary>
    /// Nivel fácil - preguntas simples que requieren conocimiento básico
    /// </summary>
    Facil = 2,

    /// <summary>
    /// Nivel intermedio - preguntas que requieren comprensión y análisis
    /// </summary>
    Intermedio = 3,

    /// <summary>
    /// Nivel difícil - preguntas complejas que requieren conocimiento avanzado
    /// </summary>
    Dificil = 4,

    /// <summary>
    /// Nivel muy difícil - preguntas expertas que requieren conocimiento profundo
    /// </summary>
    MuyDificil = 5
}